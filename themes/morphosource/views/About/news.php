<H2>New Year’s Update (January 2017)</H2>

<p><span class="blueText"><b>What are we?</b></span><br/>
A virtual museum and archive for researcher-contributed 3D data representing natural history objects, primarily funded by <a href="https://www.duke.edu/" target="_blank">Duke University</a>.
</p>

<p><span class="blueText"><b>Growth</b></span>
<br/>We’ve grown a lot in the last couple years. 
	<ul>
		<li><b>3,311</b> registered users from <b>84</b> countries around the world.</li>
		<li><b>231</b> data projects.</li>
		<li><b>19,000</b> digital datasets representing <b>5,000</b> specimens of <b>1,000</b> species from <b>125</b> institutions.</li>
	</ul>
</p>
<p>
And the community is using the site:
	<ul>
		<li><b>40,000</b> downloads.</li>
		<li><b>380,000</b> visits.</li>
		<li>MorphoSource links and citations have appeared in <i>Nature, Nature Scientific Data, PloS One, Journal of Human Evolution, Current Biology, eLife, American Journal of Physical Anthropology, Paleontologica Electronica, Biodiversity Data Journal, PeerJ, Proceedings of the Royal Society of London: B, Journal of Paleontology, Journal of Mammalian Evolution, Fishery Bulletin,</i> and more.</li>
	</ul>
</p>
<p>
	<span class="blueText"><b>Mission</b></span>
	<ol>
		<li>To provide unprecedented access to the internal and external anatomy of specimens from the world’s universities and natural history institutions by serving them as 3D digital datasets online. </li>
		<li>To provide researchers with a venue for archiving and sharing the 3D data they collect for their published research in the interest of helping them better document their work and meet the requirements of open access science, while maximizing the downstream utility of those data by maintaining them in a data schema that emulates the organization a physical repository.</li>
		<li>To align the career interests of scientists with the goals of science by providing a venue where researchers can get scholarly credit for sharing their data.</li>
		<li>To magnify the impact of museum holdings and researcher 3D data on society at large by allowing the public to access this archive for education and personal growth.</li>
	</ol>
</p>

