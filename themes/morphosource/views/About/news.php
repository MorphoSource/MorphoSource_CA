<p>
	<span class="blueText"><b>What’s great about MorphoSource</b></span>
	<ol>
		<li>For downloaders, the archive is designed to be efficient and intuitive for finding datasets of interest based on taxonomy, anatomical region, data type, or bibliography (in addition to other categories).</li>
		<li>For contributors, the ability to share data with different levels of access: Data can be published with restricted download access so that data owners can verify their approval of re-use intentions before releasing the data.  This is good researchers who have data from museums that control third party permissions.</li>
		<li>For contributors, the ability to track every download (regardless of access level) as well as the intended use of each download to build evidence of the ‘value’ of their data for research and outreach</li>
		<li>For developing research, data projects can be privately shared only among specified groups of collaborators who add to and use them</li>
		<li>For everyone, the generation of GUID’s and DOI’s for each dataset on the site, allowing unambiguous reference of data sources in publications. Datasets with DOI’s can also build formal impact factors</li>
	</ol>
</p>
<p>
	<span class="blueText"><b>What’s new on MorphoSource</b></span>
	<ol>
		<li>Private sharing of a single media groups between individual users.</li>
		<li>Exportable search results, including stats on media downloads and use cases for any published media files. To use this just type in search terms (e.g., “skull reptile” or “AMNH”) and then click the ‘export results’ link.</li>
		<li>Hierarchical <a href="http://www.morphosource.org/browse/index">taxonomic browsing</a> via linkages with <a href="http://www.vertnet.org" target="_blank">Vertnet</a></li>
		<li><a href="http://www.morphosource.org/About/API">API</a> with data dictionary and Darwin Core mapping</li>
		<li>Lots of new datasets (see above).</li>
		<li>When uploading datasets to MorphoSource, they can be formally attributed to an existing MorphoSource media group when appropriate, regardless of ownership.</li>
	</ol>
</p>
